
$("#vzrokiBtn").click(function() {
	$("#vzrokiModal").css('display','flex');
});
$("#vzrokiModal span").click(function() {
	$("#vzrokiModal").hide();
});
$("#vzrokiModal").click(function(event) {
	if(event.target === this) {
		$("#vzrokiModal").hide();
	}
});


